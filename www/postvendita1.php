<p>
    <b>SOSTITUZIONE ARTICOLO CONSEGNATO NON FUNZIONATE (DOA)</b><br />
La procedura DOA (Dead On Arrival) può essere attivata quando il componente acquistato non risulti funzionante già dal primo utilizzo.
<br /><br />
Questa Garanzia può essere esercitata comunicando il problema entro 2 giorni dal ricevimento della merce.
<br /><br />
L'attivazione del servizio potrà essere effettuata mediante contatto con il nostro Servizio Clienti.
<br /><br />
Una volta attivata, il Servizio Clienti di Fantàsia.altervista.org proporrà al Cliente, sulla base della disponibilità dell'articolo DOA in quel momento, la sostituzione del componente guasto o l'emissione di una Nota di accredito con contestuale bonifico bancario sul conto corrente del Cliente. L'importo della eventuale nota di accredito e del bonifico sarà calcolato tenendo in considerazione eventuali sconti e promozioni presenti nella fattura d'acquisto e in ogni caso la sostituzione dell'articolo avverrà entro i 60gg successivi all'apertura della pratica.
<br /><br />
L'articolo guasto dovrà essere spedito a mezzo corriere all'indirizzo che il Servizio Clienti vi indicherà. Le spese di trasposto saranno a carico di Fantàsia.altervista.org.
<br /><br />
La merce oggetto di DOA dovrà essere rispedita utilizzando una scatola rigida che contenga la confezione originale (salvo eccezioni autorizzate; es. pacchi molto voluminosi); la quale dovrà essere ASSOLUTAMENTE :
</p>
<ul class="black">
    <li>priva di qualunque scritta o strappo
    <li>completa di tutto il materiale a corredo
    <li>completa delle protezioni interne per preservarne il buono stato durante il trasporto
</ul>
<p>
In assenza delle sopracitate condizioni la merce verrà rispedita al cliente a sue spese e non sarà possibile la messa in atto del servizio DOA.
</p>