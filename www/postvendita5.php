    <b>POSSO ESERCITARE IL DIRITTO DI RECESSO?</b> <br />
La normativa prevede che il diritto di recesso sia esercitabile dalle sole persone fisiche (consumatori) che agiscono per scopi che possono considerarsi estranei alla propria attività commerciale. Il diritto di recesso, quindi, non può essere esercitato dalle persone giuridiche e dalle persone fisiche che agiscono per scopi legati ad una attività commerciale. Restano esclusi dal diritto di recesso anche gli acquisti effettuati da rivenditori o da soggetti che a qualsiasi titolo acquistano per la rivendita a terzi.
 <br /> <br />

<b>IN COSA CONSISTE?</b> <br />
Il consumatore ha diritto di recedere dal contratto entro il termine di dieci giorni lavorativi dal ricevimento della merce, restituendo il bene oggetto del recesso al venditore, il quale gli rimborserà il prezzo della merce restituita. Si precisa che nelle ipotesi di operazioni a premio (le cd. promozioni), in cui l'acquisto di un bene è abbinato ad un altro bene che viene venduto ad un prezzo irrisorio o addirittura regalato, il diritto di recesso sarà legittimamente esercitato con la restituzione di entrambi i beni oggetto dell'acquisto, stante il vincolo dell'accessorietà del bene in promozione rispetto al primo.
 <br /> <br />

<b>COSA ESCLUDE?</b> <br />
Il diritto di recesso esclude tutti gli acquisti la cui consegna si perfeziona all'interno di un punto vendita. Il diritto di recesso non si applica ai prodotti audiovisivi o software informatici consegnati sigillati, che sono stati aperti dal consumatore.
Il diritto di recesso si applica ai beni acquistati e quindi esclude le spese di consegna al destinatario che costituiscono un servizio di cui quest'ultimo ha usufruito.

 <br /> <br />
<b>COME SI ESERCITA IL DIRITTO DI RECESSO?</b> <br />
E' necessario avere a disposizione i seguenti documenti e dati: </p>
<ul class="black">
    <li>fattura originale;</li>
    <li>in caso di recesso parziale, oltre alla fattura di acquisto, il codice articolo/i;</li>
    <li>coordinate bancarie sulle quali ottenere il bonifico (Cod. IBAN - Conto Corrente dell'intestatario della fattura);</li>
</ul><p>
A questo punto si può contattare il Servizio Clienti di Fantàsia.altervista.org, che Vi informerà della procedura da adottare per aprire la pratica di recesso, dandovi contestualmente l'autorizzazione per la restituzione dei componenti. 
    
